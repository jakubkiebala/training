class Calculator:
    def add(self, a, b):
        return a + b

    def subtract(self, a, b):
        return a - b

    def multiply(self, a, b):
        return a * b

    def divide(self, a, b):
        return a / b

    def raise_to_power(self, a, b):
        return a ** b

    def root(self, a, b):
        return a ** (1 / b)
